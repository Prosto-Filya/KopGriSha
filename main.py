# Словари с коэффициентами для цвета и детали
color_coefficients = {
    "белый": 1,
    "синий": 1,
    "желтый": 1.1,
    "красный": 1,
    "перламутровый": 1.2,
    "серый металлик": 1.3
}

part_coefficients = {
    "капот": 1,
    "передняя дверь": 1.2,
    "задняя дверь": 1.1,
    "передний бампер": 1,
    "задний бампер": 1,
    "крыша": 1.1
}

# Базовая стоимость покраски
base_cost = 12000

# Ввод данных от пользователя
part = input("Введите наименование детали: ").lower()
color = input("Введите цвет: ").lower()

# Проверка наличия введенных данных в словарях
if part not in part_coefficients:
    print("Ошибка: Деталь не найдена в списке.")
elif color not in color_coefficients:
    print("Ошибка: Цвет не найден в списке.")
else:
    # Расчет стоимости покраски
    total_cost = base_cost * part_coefficients[part] * color_coefficients[color]
    print(f"Стоимость покраски детали '{part}' в цвет '{color}' составляет: {total_cost:.2f} рублей.")